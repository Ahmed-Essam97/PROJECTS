void Ultra_Sonic_Trigger(void)
{
	DIO_WriteChennel(PORTD_PIN0,PIN_LOW);
	_delay_us(5);
	DIO_WriteChennel(PORTD_PIN0,PIN_HIGH);
	_delay_us(10);
	DIO_WriteChennel(PORTD_PIN0,PIN_LOW);
}
uint16 Ultra_Sonic_GetDist(void)
{
	uint32 Time = 0;
	uint16 Distance = 0;
	Time = ICU_Capture_Time();     
	      
	if (Time >= 0)
	{
		Distance = (0.034 * Time)/2;
	}
	else Distance = 0;
	

	return Distance;
	
	
}

